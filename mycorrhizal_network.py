#   or members of a global object,
hypha = Hypha(surface_y)

###############################################
def light_led(rgb_obj):
  rgb.fill(rgb_obj)
  rgb.write()
  
def display_info(text):
  print(text)
  oled.DispChar(text, 0, 0)
  oled.show()
  
def display_tree():
  tsize = 50
  midpoint = oled_width // 2
  tx = (midpoint - tsize) // 2
  ty = (oled_height - tsize) // 2
  oled.Bitmap(tx, ty, trees[0], tsize, tsize, 0)
  oled.Bitmap(tx + midpoint, ty, trees[1], tsize, tsize, 0)
  oled.line(0, surface_y, oled_width, surface_y, 0)
  
def display_hypha():
  size = len(hypha.ys)
  
  if size > 0:
    px = hypha.x0
    py = hypha.y0
    
    for i in range(0, size):
      nx = px + hypha.dx
      ny = hypha.ys[i]
      oled.line(px, py, nx, ny, 0)
      px = nx
      py = ny
      
      if i == hypha.signal:
        oled.fill_circle(nx, ny, 2, 0)
      
    if hypha.okay:
      oled.line(px, py, hypha.xn, hypha.y0, 0)
  
def oled_refresh():
  oled.fill(255)
  display_tree()
  display_hypha()
  oled.show()

###############################################
def on_tick(_):
  if not hypha.okay:
    if hypha.x0 > 0:
      size = len(hypha.ys)

      if size % 2 == 0:
        light_led((0, 0, 255))
      else:
        light_led((0, 0, 0))

      if size > 0:
        py = hypha.ys[-1]
      else:
        py = hypha.y0
      
      ny = py + randint(-1, 1)
      
      if (ny >= oled_height) or (ny <= surface_y):
        hypha.ys.append(py)
      else:
        hypha.ys.append(ny)
      
      if size > (oled_width // 2):
        light_led((0, 255, 0))
        hypha.okay = True
        
      oled_refresh()
  else:
    if light.read() < 100:
      light_led((255, 0, 0))
      if hypha.signal < 0:
        hypha.signal = 0
    else:
      light_led((0, 0, 0))
    
    if hypha.signal >= 0:
      if hypha.signal >= len(hypha.ys):
        hypha.signal = -1
        light_led((0, 255, 0))
      else:
        hypha.signal = hypha.signal + 1
    
      oled_refresh()

def on_A_pressed(_):
  if not button_a.value() == 1:
    if not hypha.okay:
      if hypha.x0 == 0:
        hypha.x0 = oled_width // 4
        hypha.xn = oled_width // 2 + hypha.x0
        hypha.dx = 1

def on_B_pressed(_):
  if not button_b.value() == 1:
    if not hypha.okay:
      if hypha.x0 == 0:
        hypha.x0 = 3 * oled_width // 4
        hypha.xn = hypha.x0 - oled_width // 2
        hypha.dx = -1
    
###############################################
def timer_start():
  t = Timer(1)
  t.init(period=100, mode=Timer.PERIODIC, callback=on_tick)
  
def user_action_start():
  button_a.irq(trigger=Pin.IRQ_FALLING, handler=on_A_pressed)
  button_b.irq(trigger=Pin.IRQ_FALLING, handler=on_B_pressed)
  
###############################################
def main():
  oled_refresh()
  light_led((0, 0, 255))
  user_action_start()
  timer_start()

###############################################
if __name__ == "__main__":
  main()


